public class Main {
    public static void main(String[] args) {
        byte a = 10, b = 2;
        int Result;

        System.out.println("Вхідні дані: a = 10; b = 2");

        Result = a * b;
        System.out.println("a * b = " + Result);

        Result = a / b;
        System.out.println("a / b = " + Result);

        Result = a + b;
        System.out.println("a + b = " + Result);

        Result = a - b;
        System.out.println("a - b = " + Result);

        Result = a % b;
        System.out.println("a % b = " + Result);

    }
}